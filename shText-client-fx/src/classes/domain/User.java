package classes.domain;

public class User {
    int uid;
    String uname;

    public int getUid() {
        return uid;
    }

    public String getUname() {
        return uname;
    }

    public void setUid(int uid) {
        this.uid = uid;
    }

    public void setUname(String uname) {
        this.uname = uname;
    }
}
